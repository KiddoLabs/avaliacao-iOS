#import "UIColor+GBColor.h"

@implementation UIColor (GBColor)

+ (UIColor *)GBRedColor {
    
    return [UIColor colorWithRed:155.0f/255.0f green:8.0f/255.0f blue:1.0f/255.0f alpha:1.0f];
}

+ (UIColor *)GBGrayColor {
    
    return [UIColor colorWithRed:183.0f/255.0f green:183.0f/255.0f blue:183.0f/255.0f alpha:1.0f];
}

@end
